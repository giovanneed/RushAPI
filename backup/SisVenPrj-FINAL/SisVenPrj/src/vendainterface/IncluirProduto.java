    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         if ( validarAtualizacao()==false) return;

        Produto objProduto;
        try {
            objProduto = portControl.ConsultarProdutoUltimo();
            int cod = objProduto.getCodProduto()+1;

            Produto objProdutoIncluir =
                    new Produto(cod,
                            jTextFieldNome.getText(),
                            Integer.parseInt((jTextFieldCodUnidade.getText())),
                            Double.parseDouble(jFormattedTextFieldPreco.getText().replace("R$", "").replace(".", "").replace(",", ".")),
                            new GregorianCalendar());

            portControl.incluirProduto(objProdutoIncluir);
            JOptionPane.showMessageDialog(this,"Vendedor Cadastrado","Inclusão de  Produto",JOptionPane.INFORMATION_MESSAGE);
            limpaform();


        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(),"Inclusão de Produto",JOptionPane.WARNING_MESSAGE);
        }




    }//GEN-LAST:event_jButton1ActionPerformed

