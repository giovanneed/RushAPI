    private boolean validarAtualizacao(){
      if ( jTextFieldNome.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Falta informar o nome do Produto." ,
                             "Cadastro de Produto", JOptionPane.WARNING_MESSAGE);
            jTextFieldNome.requestFocus();
            return false;
        }
      return true;
    }

    private void limpaform() throws SQLException{
        jTextFieldNome.setText("  ");
        Produto objProduto = portControl.ConsultarProdutoUltimo();
        int cod = objProduto.getCodProduto()+1;
        jTextFieldCodigo.setText(""+cod);
        jTextFieldCodigo.setEditable(false);
        jTextFieldCodUnidade.setText("1");
        jFormattedTextFieldPreco.setText("R$ 00,00");
        jFormattedTextFieldData.setText(LtpUtil.formatarData(new GregorianCalendar(), "dd/MM/yyyy"));

    }


