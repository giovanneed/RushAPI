    private boolean validarAtualizacao(){
      if ( jTextFieldNome.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Falta informar o nome do Vendedor." ,
                             "Cadastro de Vendedor", JOptionPane.WARNING_MESSAGE);
            jTextFieldNome.requestFocus();
            return false;
        }
      return true;
    }

