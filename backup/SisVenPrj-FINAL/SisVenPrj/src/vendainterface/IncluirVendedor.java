    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         if ( validarAtualizacao()==false) return;

        Vendedor objVendedor;
        try {
            objVendedor = portControl.ConsultarVendedorUltimo();
            int cod = objVendedor.getCodVendedor()+1;

            Vendedor objVendedorIncluir =
                    new Vendedor(cod, jTextFieldNome.getText() , new GregorianCalendar());

            portControl.incluirVendedor(objVendedorIncluir);
            JOptionPane.showMessageDialog(this,"Vendedor Cadastrado","Inclusão de  Vendedor",JOptionPane.INFORMATION_MESSAGE);
            limpaform();


        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(),"Inclusão de Vendedor",JOptionPane.WARNING_MESSAGE);
        }




    }//GEN-LAST:event_jButton1ActionPerformed

