    private boolean validarAtualizacao(){
        if ( jTextNome.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Falta informar o nome do Cliente." ,
                             "Cadastro de Clientes", JOptionPane.WARNING_MESSAGE);
            jTextNome.requestFocus();
            return false;
        }

        if (jTextFieldEndereco.getText().equals("")){
            jTextFieldEndereco.setText("  ");

        }
        if (jTextFieldBairro.getText().equals("")){
            jTextFieldBairro.setText("  ");

        }
        if (jTextFieldCidade.getText().equals("")){
            jTextFieldCidade.setText("  ");

        }
        if (jTextFieldUF.getText().equals("")){
            jTextFieldUF.setText("  ");

        }
        if (jTextFieldCEP.getText().equals("")){
            jTextFieldCEP.setText("  ");

        }
        if (jTextFieldEmail.getText().equals("")){
            jTextFieldEmail.setText("  ");

        }
        

        return true;

    }

    private void limpaform() throws SQLException{
            jTextNome.setText("  ");
            jTextFieldEndereco.setText("  ");
            jTextFieldBairro.setText("  ");
            jTextFieldCidade.setText("  ");
            jTextFieldUF.setText("  ");
            jTextFieldCEP.setText("  ");
            jTextFieldEmail.setText("  ");
            Cliente objCliente;
            objCliente = portControl.ConsultarClienteUltimo();
            int cod = objCliente.getCodCliente()+1;
            jTextFieldCodigo.setText(""+cod);
            jTextFieldCodigo.setEditable(false);
            jFormattedTextFieldData.setText(LtpUtil.formatarData(new GregorianCalendar(), "dd/MM/yyyy"));


    }


