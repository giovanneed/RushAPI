    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        if ( validarAtualizacao()==false) return;

        Cliente objCliente;
        try {
            objCliente = portControl.ConsultarClienteUltimo();
            int cod = objCliente.getCodCliente()+1;




             Cliente objClienteIncluir =
                new Cliente(cod,
                            jTextNome.getText(),
                            jTextFieldEndereco.getText(),
                            jTextFieldBairro.getText(),
                            jTextFieldCidade.getText(),
                            jTextFieldUF.getText(),
                            jTextFieldCEP.getText(),
                            jTextFieldTelefone.getText(),
                            jTextFieldEmail.getText(),
                            new GregorianCalendar() );

             portControl.incluirCliente(objClienteIncluir);
            JOptionPane.showMessageDialog(this,"Cliente Cadastrado","Inclusão de  Cadastrado",JOptionPane.INFORMATION_MESSAGE);
            limpaform();
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(),"Inclusão de Cliente",JOptionPane.WARNING_MESSAGE);

        }
    }//GEN-LAST:event_jButton1ActionPerformed

