    private void jButtonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultarActionPerformed
        // TODO add your handling code here:
         if (jTextFieldNome.getText().equals("")) {

            jScrollPaneConsVenda.setViewportView(null);
            this.setTitle("Pesquisa de Clientes" );
            JOptionPane.showMessageDialog(this, "Falta informar o nome do cliente.","Pesquisa Cliente", JOptionPane.WARNING_MESSAGE);
            jTextFieldNome.requestFocus();
        } else try {
            listaClientesNome(jTextFieldNome.getText());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(),"Consulta de Cliente",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButtonConsultarActionPerformed

