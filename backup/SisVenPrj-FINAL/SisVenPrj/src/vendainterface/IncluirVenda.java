    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         if ( validarAtualizacao()==false) return;

        Venda objVenda;
        try {
            objVenda = portControl.ConsultarVendaUltimo();
            int cod = objVenda.getCodVenda()+1;

            Venda objVendaIncluir =
                    new Venda(cod,
                            Integer.parseInt(jTextFieldCodVendedor.getText()),
                            Integer.parseInt((jTextFieldCodCliente.getText())),
                            new GregorianCalendar());

            portControl.incluirVenda(objVendaIncluir);
            JOptionPane.showMessageDialog(this,"Venda Cadastrada","Inclus찾o de  Venda",JOptionPane.INFORMATION_MESSAGE);
            limpaform();


        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(),"Inclus찾o de Venda",JOptionPane.WARNING_MESSAGE);
        }




    }//GEN-LAST:event_jButton1ActionPerformed

