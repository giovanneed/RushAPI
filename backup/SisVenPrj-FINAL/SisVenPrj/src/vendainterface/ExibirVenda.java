    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        // TODO add your handling code here:
        if(jTextFieldCodigo.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Defina a Venda","Defina a Venda",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        if ( JOptionPane.showConfirmDialog(null, "Confirmar a exclus√£o da Venda - " +
                Integer.parseInt(jTextFieldCodigo.getText()) + " ?" ,
                "Cadastro de Venda",
                JOptionPane.YES_NO_OPTION) == JOptionPane.NO_OPTION ){
            return;
        }
        try {
            portControl.excluirVenda(Integer.parseInt(jTextFieldCodigo.getText()));
            JOptionPane.showMessageDialog(this,"Venda Exclu√≠da","Venda",JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
             JOptionPane.showMessageDialog(this, ex.getMessage(),"Consulta de Venda",JOptionPane.WARNING_MESSAGE);
        }
        limpaform();

}//GEN-LAST:event_jButtonExcluirActionPerformed

    private void jButtonBuscarCodigoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarCodigoActionPerformed

        String codigo = JOptionPane.showInputDialog(
                this,
                "C√≥digo da Venda",
                "Consulta de Vendas",
                JOptionPane.QUESTION_MESSAGE);
        if (codigo == null) return;
        if (codigo.trim().equals("") ||
                !codigo.matches("[0-9]*")) {
            JOptionPane.showMessageDialog(
                    this,
                    "C√≥digo Inv√°lido.",
                    "Consulta de Vendas",
                    JOptionPane.WARNING_MESSAGE);
        }

        Venda objVenda;
        try {
            objVenda = portControl.consultarVendaCodigo(Integer.parseInt(codigo));
            
            exibirVenda(objVenda);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(),"Consulta de Venda",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButtonBuscarCodigoActionPerformed

    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed


        if (jTextFieldCodigo.getText().equals("")){
            JOptionPane.showMessageDialog(this, "N√£o Alterado","Consulta de Cliente",JOptionPane.WARNING_MESSAGE);
        }else{

        
        // TODO add your handling code here:
        Venda objVendaIncluir =
                new Venda(Integer.parseInt((jTextFieldCodigo.getText())),
                            Integer.parseInt(jTextCodVendedor.getText()),
                            Integer.parseInt((jTextFieldCodCliente.getText())),
                            new GregorianCalendar() );

        try {
            portControl.alteraVenda(objVendaIncluir);
            JOptionPane.showMessageDialog(this,"Venda Atualizado","Venda Atualizado",JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(),"Consulta de Venda",JOptionPane.WARNING_MESSAGE);
        }
        }

    }//GEN-LAST:event_jButtonAlterarActionPerformed

