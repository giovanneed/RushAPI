    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        // TODO add your handling code here:

        String codigo = JOptionPane.showInputDialog(
                this,
                "Código do Vendedor",
                "Consulta de Vendedor",
                JOptionPane.QUESTION_MESSAGE);
        if (codigo == null) return;
        if (codigo.trim().equals("") ||
                !codigo.matches("[0-9]*")) {
            JOptionPane.showMessageDialog(
                    this,
                    "Código Inválido.",
                    "Consulta de Codigo",
                    JOptionPane.WARNING_MESSAGE);
        }

        Vendedor objVendedor;
        try {
            objVendedor = portControl.consultarVendedorCodigo(Integer.parseInt(codigo));

            exibirVendedor(objVendedor);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(),"Consulta de Vendedor",JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_jButtonBuscarActionPerformed

